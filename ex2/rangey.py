# 1) Using a for loop and the range function 
# to print every other number between 0 and 100,
# starting with 0.

for i in range(0,100):
    print(i, end=' ')
print()
print()

for i in range(0,100,2):
    print(i, end=' ')
print()
print()

# 2) Using a for loop and the range function 
# to print every third number between 0 and 100,
# starting with 0.

for i in range(0,100,3):
    print(i, end=' ')
print()
